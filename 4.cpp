#include <iostream> 
using namespace std; 
 
int main() 
{ 
  int x=3, y=0; //объявление двух целых переменных
  bool f=false, v=true; //объявление двух логических переменных
  cout<<-x<<"\t"<<!x<<endl; //вывод выражения
  cout<<-y<<"\t"<<!y<<endl; //вывод выражения
  cout<<f<<"\t"<<!f<<endl; //вывод выражения(false=0)
  cout<<v<<"\t"<<!v<<endl; //вывод выражения(true=1)
  return 0; //завершение программы
}
